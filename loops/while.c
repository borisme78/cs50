#include <stdio.h>

int main() 
{
    int number; // Змінна для зберігання числа від користувача
    int sum = 0; // Змінна для зберігання суми

    printf("Введіть цілі числа. Для завершення введіть 0.\n");

    // Цикл продовжується, доки введене число не дорівнює 0
    while (1) 
    { // '1' означає true, тому цикл виконується нескінченно, доки не буде 'break'
        printf("Введіть число: ");
        scanf("%d", &number); // Зчитуємо введене число

        // Перевірка, чи ввів користувач 0 для виходу
        if (number == 0) {
            printf("Введено 0. Завершення циклу.\n");
            break; // Вихід з циклу while
        }

        sum += number; // Додаємо число до суми
        printf("Поточна сума: %d\n", sum);
    }

    printf("Загальна сума введених чисел: %d\n", sum);

    return 0;
}
/*
    Пояснення:
1. #include <stdio.h>: Підключаємо стандартну бібліотеку введення/виведення.
2. int number; int sum = 0;: Оголошуємо цілочисельні змінні: number для зберігання поточного введення, sum для накопичення суми.
3. printf(...): Виводимо інструкцію для користувача.
4. while (1): Це нескінченний цикл, оскільки 1 завжди є істинним значенням. Умова виходу буде оброблятися всередині тіла циклу.
5. scanf("%d", &number);: Зчитуємо число, введене користувачем, і зберігаємо його в number.
6. if (number == 0) { break; }: Це ключова частина. Якщо користувач вводить 0, команда break негайно припиняє виконання циклу while.
7. sum += number;: Якщо число не 0, воно додається до sum.
8. printf("Поточна сума: %d\n", sum);: Виводимо проміжну суму.
9. printf("Загальна сума...");: Виводимо остаточну суму після завершення циклу. 
Цей приклад демонструє, як цикл while може реагувати на введення користувача, повторюючи дії до тих пір, поки не буде виконано певну умову (в даному випадку, введення 0). 
*/