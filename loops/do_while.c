#include <stdio.h> // Підключаємо стандартну бібліотеку вводу/виводу

int main() 
{
    int k; // Оголошуємо змінну для зберігання числа
    
    // Починаємо цикл do-while
    do 
    {
        printf("Введіть число (від 1 до 20): "); // Запит до користувача
        scanf("%d", &k); // Зчитуємо введене число
    } 
    while (k < 1 || k > 20); // Умова: продовжувати, доки число НЕ в діапазоні (менше 1 АБО більше 20)

    printf("Ви ввели коректне число: %d\n", k); // Виводимо результат

    return 0; // Завершення програми
}
/* 
Як це працює:
1. do { ... }: Код всередині блоку do (запит числа та його зчитування) виконується першим, без перевірки умови.
2. while (k < 1 || k > 20);: Після першого виконання перевіряється умова. Якщо введене k менше 1 або більше 20 (тобто не в діапазоні), цикл повторюється.
3. Цикл завершиться лише тоді, коли користувач введе число, що задовольняє умову (наприклад, 5, 15, 20). 
*/