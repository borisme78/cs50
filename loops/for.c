#include <stdio.h> // Підключаємо бібліотеку для вводу/виводу

int main() 
{
    int n; // Змінна для зберігання числа, введеного користувачем
    int i; // Лічильник циклу

    // Запитати користувача про введення числа
    printf("Введіть ціле число: ");
    scanf("%d", &n); // Зчитати введене число

    // Цикл for, який виконується від 1 до n
    for (i = 1; i <= n; i++) 
    {
        printf("Ітерація номер: %d\n", i); // Вивести поточну ітерацію
    }

    printf("Цикл завершено.\n");

    return 0; // Повертаємо 0, щоб показати успішне виконання
}
/* 
Як працює цей код:
1. #include <stdio.h>: Підключає стандартну бібліотеку вводу/виводу, необхідну для printf та scanf.
2. int n; : Оголошується цілочисельна змінна n для зберігання числа, яке введе користувач.
3. printf("Введіть ціле число: ");: Виводить запит на екрані.
4. scanf("%d", &n);: Чекає, поки користувач введе ціле число і натисне Enter. Це число буде збережено в змінній n.
5. for (i = 1; i <= n; i++): Це сам цикл for.
6. i = 1: Ініціалізація — змінна-лічильник i починає з 1.
7. i <= n: Умова — цикл триватиме, доки i менше або дорівнює n.
8. i++: Інкрементування — після кожної ітерації i збільшується на 1.
9. printf("Ітерація номер: %d\n", i);: Друк поточної ітерації всередині тіла циклу.
10. return 0;: Завершує головну функцію. 
*/