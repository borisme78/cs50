#include <stdio.h> // Підключаємо стандартну бібліотеку вводу-виводу

int main() 
{
    int age; // Оголошуємо змінну для зберігання віку

    // Запитуємо користувача ввести вік
    printf("Будь ласка, введіть ваш вік: ");
    scanf("%d", &age); // Зчитуємо введене число і зберігаємо в змінну 'age'

    // Перевірка умови за допомогою if-else
    if (age >= 18) 
    {
        // Цей блок виконується, якщо умова (age >= 18) істинна
        printf("Вам є 18 років або більше. Ви можете переглянути фільм!\n");
    }
     else 
    {
        // Цей блок виконується, якщо умова хибна (вік < 18)
        printf("Вибачте, фільм тільки для дорослих (18+).\n");
    }

    return 0; // Позначаємо успішне завершення програми
}
/*
Пояснення коду:
1. #include <stdio.h>: Директива, що підключає стандартну бібліотеку для функцій вводу/виводу (як printf та scanf).
2. int age;: Створюється цілочисельна змінна age для збереження віку.
3. printf(...): Виводить запит користувачеві.
4. scanf("%d", &age);: Зчитує ціле число, введене користувачем, і зберігає його в age. Оператор & (адреса) необхідний, щоб scanf знала, куди зберегти значення.
5. if (age >= 18): Перевіряє, чи вік більше або дорівнює 18.
6. { ... } else { ... }: Якщо умова в if істинна, виконується код у перших дужках. Якщо хибна, виконується код у блоці else.
7. return 0;: Сигналізує операційній системі, що програма завершилась без помилок.
*/